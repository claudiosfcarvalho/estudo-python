# loops.py
# Exemplo de loops for e while

# for com range
for i in range(1, 6):
    print(f"Contador for: {i}")

# while
contador = 5
while contador > 0:
    print(f"Contador while: {contador}")
    contador -= 1

# iterando sobre lista
frutas = ["maçã", "banana", "laranja"]
for fruta in frutas:
    print("Fruta:", fruta)
